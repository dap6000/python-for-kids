money = 2000
if money > 1000 :
    print("I'm rich!")
else :
    print("I'm not rich!")
    print("But I might be later...")

# Since the value stored in the variable money is greater than 1000 the
# conditional comparision on line 2 will evaluate as true and the print
# statement on line 3 will execute. The entire else block will be skipped.
